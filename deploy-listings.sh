# ─────────────────────────────────────────────────────────────
# פונקציית עזר: יוצרת ליסטינג ומחזירה את ה-ID
# שימוש: ID=$(create_listing "שם" "תקציר" "תוכן" "קטגוריה")
# ─────────────────────────────────────────────────────────────
create_listing() {
  local TITLE="$1"
  local EXCERPT="$2"
  local CONTENT="$3"
  local CAT="$4"        # atraktziot | lina | ochel | siurim
  local CITY="$5"
  local ADDRESS="$6"
  local LAT="$7"
  local LNG="$8"
  local PHONE="$9"
  local WEBSITE="${10}"
  local RATING="${11}"
  local FEATURED="${12:-0}"

  echo ""
  echo "→ מוסיף: $TITLE"

  ID=$($SSH "$WP post create \
    --post_type=gd_place \
    --post_title='$TITLE' \
    --post_excerpt='$EXCERPT' \
    --post_content='$CONTENT' \
    --post_status=pending \
    --porcelain 2>/dev/null")

  $SSH "$WP post term set $ID gd_placecategory '$CAT' --by=slug 2>/dev/null"
  $SSH "$WP post meta add $ID geodir_post_city '$CITY' 2>/dev/null"
  $SSH "$WP post meta add $ID geodir_post_address '$ADDRESS' 2>/dev/null"
  $SSH "$WP post meta add $ID geodir_post_latitude '$LAT' 2>/dev/null"
  $SSH "$WP post meta add $ID geodir_post_longitude '$LNG' 2>/dev/null"
  [ -n "$PHONE" ]    && $SSH "$WP post meta add $ID geodir_post_phone '$PHONE' 2>/dev/null"
  [ -n "$WEBSITE" ]  && $SSH "$WP post meta add $ID geodir_post_website '$WEBSITE' 2>/dev/null"
  [ -n "$RATING" ]   && $SSH "$WP post meta add $ID geodir_post_rating '$RATING' 2>/dev/null"
  [ "$FEATURED" = "1" ] && $SSH "$WP post meta add $ID geodir_featured '1' 2>/dev/null"

  echo "   ✓ נוצר (ID: $ID, סטטוס: pending — ממתין לאישור)"
  echo "$ID"
}


